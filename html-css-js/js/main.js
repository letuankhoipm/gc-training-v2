function hiddenMainLeft() {
    var hidden = document.getElementById('hidden-main-left');
    if (hidden.style.display === 'none') hidden.style.display = 'block';
	else hidden.style.display = 'none';
}

    
function marginMainLeft() {
    var marginMainLeft = document.getElementById('margin-main-left');
    if (marginMainLeft.style.marginLeft === '15em') marginMainLeft.style.marginLeft= '5.5em';
	else marginMainLeft.style.marginLeft= '15em';
}

function marginMainLeftVideo() {
    var marginMainLeftVideo = document.getElementById('margin-main-left-video');
    if (marginMainLeftVideo.style.marginLeft === '15em') marginMainLeftVideo.style.marginLeft= '5.5em';
	else marginMainLeftVideo.style.marginLeft= '15em';
}